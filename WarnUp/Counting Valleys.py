def countingValleys(steps, path):
    
    elevation=0
    vally_count=0
    
    for steps in path:
        if steps=='U':
            elevation+=1
            if elevation==0:
                vally_count+=1
        if steps=='D':
            elevation-=1
            
    return (vally_count)

